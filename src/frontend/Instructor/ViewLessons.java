    public void initializeTableAndLoadData() {
        Title = Cdb.getCourse(courseId).getTitle();
        this.lessons = Cdb.getCourse(courseId).getLessons();
        if (this.lessons == null) {
            this.lessons = new ArrayList<Lesson>();
        }
        lessonsModel = new javax.swing.table.DefaultTableModel(
                new Object[][] {}, // Empty data initially
                new String[] { "Lesson ID", "Title", "Content" } // Column names
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Make all cells non-editable
                return false;
            }
        };
        lessonsTable.setModel(lessonsModel);
        for (int i = 0; i < lessons.size(); i++) {
            Lesson l = lessons.get(i);
            lessonsModel.addRow(new Object[] {
                    l.getLessonId(), l.getTitle(),
                    l.getContent()
            });
        }
        System.out.println("lesson table loaded , size: " + lessons.size());
    }

