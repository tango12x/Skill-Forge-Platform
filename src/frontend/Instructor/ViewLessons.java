    private void btnCreateLessonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCreateLessonActionPerformed
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Add Lesson ?",
                "Confirm addition", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            JDialog frame = new LessonEditor(courseId, this);
            frame.setVisible(true);
            frame.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
            frame.setLocationRelativeTo(null);
        }
        //wait for child to finish
        try {
            initializeTableAndLoadData();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_btnCreateLessonActionPerformed

    private void btnEditLessonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEditLessonActionPerformed
        int selectedRow = lessonsTable.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Please select a lesson to edit.",
                    "No Selection", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        String id = (String) lessonsTable.getValueAt(selectedRow, 0);

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Edit Lesson: " + lessonsTable.getValueAt(selectedRow, 1) + "?",
                "Confirm Edit", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            JDialog frame = new LessonEditor(courseId,id,this);
            frame.setVisible(true);
            frame.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
            frame.setLocationRelativeTo(null);
        }
        try {
            initializeTableAndLoadData();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_btnEditLessonActionPerformed

    private void btnDeleteLessonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteLessonActionPerformed
        int selectedRow = lessonsTable.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Please select a lesson to delete.",
                    "No Selection", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        Course c = (Course) Cdb.getCourse(courseId);
        String id = (String) lessonsTable.getValueAt(selectedRow, 0);
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Delete Lesson: " + lessonsTable.getValueAt(selectedRow, 1) + "?",
                "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            c.removeLesson(id);
            Cdb.update(c);
            Cdb.SaveCoursesToFile();
        }
        try {
            initializeTableAndLoadData();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_btnDeleteLessonActionPerformed

